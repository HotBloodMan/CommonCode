package com.ljt;

public class Person {
	private String head;
	private String body;
	private String fooot;
	
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getFooot() {
		return fooot;
	}
	public void setFooot(String fooot) {
		this.fooot = fooot;
	}
}
